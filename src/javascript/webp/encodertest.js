var deferredTestCase;
var webpEncoder;

webp.EncoderTest.install = function() {
  deferredTestCase = goog.testing.DeferredTestCase.createAndInstall(
      'webp.EncoderTest');
  deferredTestCase.stepTimeout = webp.EncoderTest.STEP_TIMEOUT_;

  webp.vp8.debug.setEnabled(true);
};

/** @type {number} */
webp.EncoderTest.STEP_TIMEOUT_ = 20000;

function setUp() {
  webpEncoder = new webp.Encoder();
}

function tearDown() {
  goog.dispose(webpEncoder);
  delete webpEncoder;
}

function testEncode20x12() {
  var canvasEl = webp.testing.encoderTestUtil.renderTestImage1(
      20, 12);
  assertTrue(webpEncoder.encode(canvasEl));
  assertEquals(TEST_ENCODE_20x12_GOLDEN_WEBP_BASE64,
      webp.util.arrayBufferToBase64(webpEncoder.getOutput()));
  webp.vp8.debug.clear();
}

function testEncodePhoto2() {
  var callbacks = new goog.async.Deferred();
  deferredTestCase.addWaitForAsync('Waiting for photo encode', callbacks);
  callbacks.addCallback(
      function() {
        return webp.testing.encoderTestUtil.renderDataUrlToCanvas(
            TEST_PHOTO2_DATA_URL);
      }).addCallback(function(canvasEl) {
        assertTrue(webpEncoder.encode(canvasEl));
        assertEquals(TEST_ENCODE_PHOTO2_GOLDEN_WEBP_BASE64,
            webp.util.arrayBufferToBase64(webpEncoder.getOutput()));
        webp.vp8.debug.clear();
      });
  deferredTestCase.waitForDeferred(callbacks);
}

