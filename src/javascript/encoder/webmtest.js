function testEncode2dVideo() {
  var time = new goog.date.Date(2013, 1, 1).getTime();
  var frameIndex = 0;
  var frameCount = 5;
  var frameDurations = [20, 30, 40, 50, 60];

  function frameGenerator(canvasEl, canvasCtx) {
    if (frameIndex >= frameCount) {
      return null;
    }
    time += 1000;
    pics3.encoder.testing.webmTestUtil.renderClock(canvasCtx, time);
    var dataUrl = new pics3.parser.DataUrl(canvasEl.toDataURL(
        pics3.PhotoMimeType.JPG));
    var duration = frameDurations[frameIndex];

    frameIndex++;
    return {
      dataUrls: [dataUrl],
      duration: duration
    };
  }

  doTestEncodeVideo(frameGenerator, TEST_ENCODE_VIDEO_GOLDEN_WEBM_BASE64, 200);
}

function testEncode3dVideo() {
  var frameIndex = 0;
  var frameCount = 5;
  var frameDurations = [100, 150, 200, 250, 300];

  function frameGenerator(canvasEl, canvasCtx) {
    if (frameIndex >= frameCount) {
      return null;
    }
    var dataUrls = [];
    for (var i = 0; i < 2; i++) {
      pics3.encoder.testing.webmTestUtil.render3dSample(canvasCtx,
          frameIndex * 5, i == 0);
      dataUrls.push(new pics3.parser.DataUrl(canvasEl.toDataURL(
          pics3.PhotoMimeType.JPG)));
    }
    var duration = frameDurations[frameIndex];

    frameIndex++;
    return {
      dataUrls: dataUrls,
      duration: duration
    };
  }

  doTestEncodeVideo(frameGenerator, TEST_ENCODE_3D_VIDEO_GOLDEN_WEBM_BASE64,
      1000);
}

function doTestEncodeVideo(frameGenerator, goldenOutputBase64,
    expectedDuration) {
  var canvasEl = document.createElement('canvas');
  canvasEl.setAttribute('width', 150);
  canvasEl.setAttribute('height', 150);
  document.body.appendChild(canvasEl);
  var canvasCtx = canvasEl.getContext('2d');

  var videoEl = document.createElement('video');
  videoEl.setAttribute('width', 150);
  videoEl.setAttribute('height', 150);
  videoEl.setAttribute('controls', 'on');
  videoEl.setAttribute('autoplay', 'on');
  document.body.appendChild(videoEl);

  var loadedDataFired = false;

  function finish(d) {
    goog.dom.removeNode(canvasEl);
    goog.dom.removeNode(videoEl);
    d.callback();
  }

  function finalizeVideo(d) {
    webm.compile(true);
    var url = pics3.encoder.util.createObjectUrl(webm.getOutputAsBlob());
    videoEl.addEventListener('loadeddata', function() {
      loadedDataFired = true;
      assertEquals(150, videoEl.videoWidth);
      assertEquals(150, videoEl.videoHeight);
    }, false);
    videoEl.addEventListener('error', function(e) {
      fail('Video error: ' + e);
    }, false);
    videoEl.addEventListener('ended', function() {
      assertTrue(loadedDataFired);
      assertEquals(0, videoEl.startTime);
      assertEquals(expectedDuration, Math.round(videoEl.duration * 1000));
      assertEquals(goldenOutputBase64,
          pics3.parser.base64.fromUint8Array(webm.getOutput()));
      finish(d);
    }, false);
    videoEl.src = url;
  }

  function renderNextFrame(d) {
    var frameInfo = frameGenerator(canvasEl, canvasCtx);
    if (!frameInfo) {
      finalizeVideo(d);
      return;
    }
    var webp = new pics3.encoder.Webp();
    webp.encodeFromDataUrls(frameInfo.dataUrls).addCallback(function() {
      var frame = pics3.encoder.Webm.Frame.newFrame(webp.getImage(),
          frameInfo.duration);
      if (frameInfo.dataUrls.length == 2) {
        frame.setStereoSideBySide(true);
      }
      webm.addFrame(frame);
      renderNextFrame(d);
    });
  }

  var callbacks = new goog.async.Deferred();
  deferredTestCase.addWaitForAsync('Waiting for video encode', callbacks);
  callbacks.addCallback(function() {
    var d = new goog.async.Deferred();
    renderNextFrame(d);
    return d;
  });
  deferredTestCase.waitForDeferred(callbacks);
}

